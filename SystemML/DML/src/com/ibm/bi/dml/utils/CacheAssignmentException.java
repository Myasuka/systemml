package com.ibm.bi.dml.utils;

public class CacheAssignmentException extends CacheException
{
	public CacheAssignmentException ()
	{
		super ();
	}

	public CacheAssignmentException (String message)
	{
		super (message);
	}

	public CacheAssignmentException (Exception cause)
	{
		super (cause);
	}

	public CacheAssignmentException (String message, Exception cause)
	{
		super (message, cause);
	}

}
